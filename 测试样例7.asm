;试编写一段程序，求出首地址为DATA的100D字数组中的最小偶数，并把它存放在AX中
DATAS SEGMENT
	;此处输入数据段代码  
	MIN DW 0
	ARY DW 10 DUP(1,4,3,2,5,6,7,8,9,10)
DATAS ENDS

STACKS SEGMENT
	;此处输入堆栈段代码
STACKS ENDS

CODES SEGMENT
	ASSUME CS:CODES,DS:DATAS,SS:STACKS
START:
	MOV AX,DATAS
	MOV DS,AX
	;此处输入代码段代码
	MOV BX,0
	MOV CX,100
	LEA BX,ARY
COMPARE:
	MOV AX,[BX]
	ADD BX,2
	TEST AX,01H
	LOOPNZ COMPARE
	JNZ STOP
	JCXZ STOP;jcxz的转移条件是cx=0
COMPARE1:
	MOV DX,[BX]
	ADD BX,2
	TEST DX,01H
	JNZ NEXT
	CMP AX,DX
	JLE NEXT
	MOV AX,DX
NEXT:
	LOOP COMPARE1
STOP:  
	MOV MIN,AX
	MOV AH,4CH
	INT 21H
CODES ENDS
	END START
